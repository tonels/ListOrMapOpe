package hb.model;


public class GroupProcess {

  private Long groupProcessId;
  private String groupId;
  private String processJson;


  public Long getGroupProcessId() {
    return groupProcessId;
  }

  public void setGroupProcessId(Long groupProcessId) {
    this.groupProcessId = groupProcessId;
  }


  public String getGroupId() {
    return groupId;
  }

  public void setGroupId(String groupId) {
    this.groupId = groupId;
  }


  public String getProcessJson() {
    return processJson;
  }

  public void setProcessJson(String processJson) {
    this.processJson = processJson;
  }

}
